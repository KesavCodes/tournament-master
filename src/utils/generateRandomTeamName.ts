export const generateCoolTeamNames = (noOfTeams: number): string[] => {
  const coolTeamNames = new Set<string>();
  while (coolTeamNames.size < noOfTeams) {
    const adj = adjectives[Math.floor(Math.random() * adjectives.length)];
    const noun = nouns[Math.floor(Math.random() * nouns.length)];
    coolTeamNames.add(`${adj} ${noun}`);
  }
  return Array.from(coolTeamNames);
};
